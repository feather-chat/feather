/** Map from alias/shortcode to emoji character. */
export const EMOJI_MAP: Record<string, string> = (() => {
  const map: Record<string, string> = {};
  for (const category of EMOJI_CATEGORIES) {
    for (const entry of category.emojis) {
      for (const alias of entry.aliases) {
        map[alias] = entry.emoji;
      }
    }
  }
  return map;
})();

/** Reverse map from emoji character to primary alias. */
export const EMOJI_NAME: Record<string, string> = (() => {
  const map: Record<string, string> = {};
  for (const category of EMOJI_CATEGORIES) {
    for (const entry of category.emojis) {
      map[entry.emoji] = entry.aliases[0];
    }
  }
  return map;
})();

/** Regex matching any known Unicode emoji (longest-first for correct multi-codepoint matching). */
export const UNICODE_EMOJI_RE: RegExp = (() => {
  const escaped = Object.keys(EMOJI_NAME)
    .sort((a, b) => b.length - a.length)
    .map((e) => e.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'));
  return new RegExp(`(?:${escaped.join('|')})`);
})();

/** Unicode skin-tone modifiers (Fitzpatrick scale). */
export type SkinTone = '' | '\u{1F3FB}' | '\u{1F3FC}' | '\u{1F3FD}' | '\u{1F3FE}' | '\u{1F3FF}';
export const SKIN_TONES: { tone: SkinTone; label: string; swatch: string }[] = [
  { tone: '', label: 'Default', swatch: '✋' },
  { tone: '\u{1F3FB}', label: 'Light', swatch: '✋🏻' },
  { tone: '\u{1F3FC}', label: 'Medium-Light', swatch: '✋🏼' },
  { tone: '\u{1F3FD}', label: 'Medium', swatch: '✋🏽' },
  { tone: '\u{1F3FE}', label: 'Medium-Dark', swatch: '✋🏾' },
  { tone: '\u{1F3FF}', label: 'Dark', swatch: '✋🏿' },
];

/** Apply a skin tone modifier to an emoji that supports it. */
export function applySkinTone(emoji: string, tone: SkinTone): string {
  if (!tone) return emoji;
  // Insert modifier after the first code point (base emoji).
  // Some emojis use ZWJ sequences — we insert the modifier after the first non-VS16 code point.
  const codePoints = [...emoji];
  if (codePoints.length === 0) return emoji;
  // Skip variation selector U+FE0F if present at index 1
  const insertIdx = codePoints[1] === '\uFE0F' ? 2 : 1;
  return codePoints.slice(0, insertIdx).join('') + tone + codePoints.slice(insertIdx).join('');
}

