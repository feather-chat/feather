/** Apply a skin tone modifier to an emoji that supports it. */
export function applySkinTone(emoji: string, tone: SkinTone): string {
  if (!tone) return emoji;
  // Insert modifier after the first code point (base emoji).
  // Some emojis use ZWJ sequences â€” we insert the modifier after the first non-VS16 code point.
  const codePoints = [...emoji];
  if (codePoints.length === 0) return emoji;
  // Skip variation selector U+FE0F if present at index 1
  const insertIdx = codePoints[1] === '\uFE0F' ? 2 : 1;
  return codePoints.slice(0, insertIdx).join('') + tone + codePoints.slice(insertIdx).join('');
}

