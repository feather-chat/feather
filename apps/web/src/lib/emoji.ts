/**
 * Search emojis by query string against aliases and tags.
 * Returns matching emojis with their primary alias.
 */
export function searchEmojis(
  query: string,
  limit = 10,
): Array<{ shortcode: string; emoji: string }> {
  if (!query) {
    return COMMON_EMOJIS.slice(0, limit).map((emoji) => {
      const shortcode = EMOJI_NAME[emoji] || '';
      return { shortcode, emoji };
    });
  }

  const lowerQuery = query.toLowerCase();
  const results: Array<{ shortcode: string; emoji: string; score: number }> = [];

  for (const category of EMOJI_CATEGORIES) {
    for (const entry of category.emojis) {
      let bestScore = Infinity;
      let bestAlias = entry.aliases[0];

      for (const alias of entry.aliases) {
        if (alias === lowerQuery) {
          bestScore = 0;
          bestAlias = alias;
          break;
        }
        if (alias.startsWith(lowerQuery) && bestScore > 1) {
          bestScore = 1;
          bestAlias = alias;
        } else if (alias.includes(lowerQuery) && bestScore > 2) {
          bestScore = 2;
          bestAlias = alias;
        }
      }

      if (bestScore === Infinity) {
        for (const tag of entry.tags) {
          if (tag === lowerQuery) {
            bestScore = 3;
            break;
          }
          if (tag.startsWith(lowerQuery) && bestScore > 4) {
            bestScore = 4;
          } else if (tag.includes(lowerQuery) && bestScore > 5) {
            bestScore = 5;
          }
        }
      }

      if (bestScore < Infinity) {
        results.push({
          shortcode: bestAlias,
          emoji: entry.emoji,
          score: bestScore,
        });
      }
    }
  }

  return results
    .sort((a, b) => a.score - b.score || a.shortcode.localeCompare(b.shortcode))
    .slice(0, limit)
    .map(({ shortcode, emoji }) => ({ shortcode, emoji }));
}

/**
 * Resolve a standard emoji shortcode to its Unicode character.
 * Returns undefined if the shortcode doesn't match any standard emoji.
 */
export function resolveStandardShortcode(name: string): string | undefined {
  return EMOJI_MAP[name];
}

export interface SearchResult {
  shortcode: string;
  emoji?: string;
  isCustom: boolean;
  imageUrl?: string;
}

/**
 * Search both standard and custom emojis.
 * Returns merged results with custom emojis marked.
 */
export function searchAllEmojis(
  query: string,
  limit: number,
  customEmojis: Array<{ name: string; url: string }>,
): SearchResult[] {
  const standardResults = searchEmojis(query, limit).map((r) => ({
    shortcode: r.shortcode,
    emoji: r.emoji,
    isCustom: false,
  }));

  if (!customEmojis.length) return standardResults;

  const lowerQuery = query.toLowerCase();
  const customResults: SearchResult[] = customEmojis
    .filter((e) => !query || e.name.includes(lowerQuery))
    .map((e) => ({
      shortcode: e.name,
      isCustom: true,
      imageUrl: e.url,
    }));

  // Interleave: custom matches first if exact, then standard, then partial custom
  const exact: SearchResult[] = [];
  const startsWith: SearchResult[] = [];
  const contains: SearchResult[] = [];

  for (const r of customResults) {
    if (r.shortcode === lowerQuery) exact.push(r);
    else if (r.shortcode.startsWith(lowerQuery)) startsWith.push(r);
    else contains.push(r);
  }

  return [...exact, ...startsWith, ...standardResults, ...contains].slice(0, limit);
}
