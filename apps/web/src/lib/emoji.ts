/** Map from alias/shortcode to emoji character. */
export const EMOJI_MAP: Record<string, string> = (() => {
  const map: Record<string, string> = {};
  for (const category of EMOJI_CATEGORIES) {
    for (const entry of category.emojis) {
      for (const alias of entry.aliases) {
        map[alias] = entry.emoji;
      }
    }
  }
  return map;
})();

/** Reverse map from emoji character to primary alias. */
export const EMOJI_NAME: Record<string, string> = (() => {
  const map: Record<string, string> = {};
  for (const category of EMOJI_CATEGORIES) {
    for (const entry of category.emojis) {
      map[entry.emoji] = entry.aliases[0];
    }
  }
  return map;
})();

/** Regex matching any known Unicode emoji (longest-first for correct multi-codepoint matching). */
export const UNICODE_EMOJI_RE: RegExp = (() => {
  const escaped = Object.keys(EMOJI_NAME)
    .sort((a, b) => b.length - a.length)
    .map((e) => e.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'));
  return new RegExp(`(?:${escaped.join('|')})`);
})();

